# Arithmetic Operator 

# using the addition operator (+)
var1 = 3
var2 = 5

res = var1 + var2
print(res)


# using the subtraction operator (-)
var1 = 10
var2 = 6

res = var1 - var2
print(res)


# using the multiplication operator
var1 = 2
var2 = 1

res = var1 * var2
print(res)


# using the division operator
var1 = 8
var2 = 2

res = var1 /var2
print(res)


# using the modulus operator
var1 = 7
var2 = 2

res = var1 % var2
print(res)


# using the exponentiation operator
var1 = 4
var2 = 2

res = var1 ** var2
print(res)


# using the floor division
var1 = 22
var2 = 2

res = var1 // var2
print(res)