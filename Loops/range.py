# range in python

r = range(10,20)  # range()
print(type(r))
print(r)
l = list(r)   # list()
print(l)



r = range(-2,2)   # negative value in range()
print(r)
l = list(r)       # list
print(l)



r = range(10,20,2) # three parameter in range()
print(r)
l = list(r)
print(l)



r = range(10,20,3) # three parameter in range()
print(r)
l = list(r)
print(l)



r = range(20,10,-2) # negative three parameter in range()
print(r)
l = list(r)
print(l)



r = range(20,10,-3) # negative three parameter in range()
print(r)
l = list(r)
print(l)